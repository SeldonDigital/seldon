/**
 * Dynamic theme token catalog entries for token tables that depend on the loaded theme
 * (swatches, shadow recipes, font presets, â€¦). Entries may use `propertyKey` â†’ `PROPERTY_SCHEMAS`;
 * see `helpers/resolve-theme-token-schema.ts`. Static entries live in `theme-static-schemas.ts`.
 */
import { getDynamicSwatchName } from "../../compute/get-dynamic-swatch-names"
import type { ComputedTheme, StockTheme } from "../../types/theme"
import type {
  ThemeTokenCatalogDraft,
  ThemeTokenSchema,
  ThemeTokenSchemaUnresolved,
} from "../../types/schema"
import type { StockThemeSwatch, ThemeSwatch } from "../../values"
import { isDynamicSwatchToken } from "../../values"
import { finalizeThemeTokenSchema } from "../helpers/finalize-theme-token-schema"

export type ThemeOrStock = StockTheme | ComputedTheme

function swatchSchemaLabel(
  swatch: StockThemeSwatch | ThemeSwatch,
  theme: ThemeOrStock,
): string {
  if (isDynamicSwatchToken(swatch)) {
    return getDynamicSwatchName(swatch.role, theme)
  }
  return (swatch as ThemeSwatch).name ?? ""
}

/**
 * Generate schemas for swatch properties.
 *
 * Slots are emitted in three groups, in order:
 *   1. Dynamic palette slots (`white`, `gray`, â€¦, `swatch4`) â€” labels generated by
 *      {@link getDynamicSwatchName} from the theme's harmony.
 *   2. Reserved fixed slots (`background`).
 *   3. Free-named user swatches â€” any other key in the swatch table. The cell's
 *      `name` is the source of truth; an empty string falls back here.
 */
export function generateSwatchSchemas(
  theme: ThemeOrStock,
): ThemeTokenSchemaUnresolved[] {
  const schemas: ThemeTokenSchemaUnresolved[] = []
  let order = 0
  const swatchTable = theme.swatch as Record<
    string,
    StockThemeSwatch | ThemeSwatch
  >

  const dynamicSlots = [
    "white",
    "gray",
    "black",
    "primary",
    "swatch1",
    "swatch2",
    "swatch3",
    "swatch4",
  ]
  const RESERVED_SLOTS = ["background"]

  for (const swatchId of dynamicSlots) {
    const swatch = swatchTable[swatchId]
    if (swatch) {
      schemas.push({
        key: `swatch.${swatchId}`,
        propertyKey: "color",
        label: swatchSchemaLabel(swatch, theme),
        section: "swatch",
        order: order++,
      })
    }
  }

  for (const swatchId of RESERVED_SLOTS) {
    const swatch = swatchTable[swatchId]
    if (swatch) {
      schemas.push({
        key: `swatch.${swatchId}`,
        propertyKey: "color",
        label: swatchSchemaLabel(swatch, theme),
        section: "swatch",
        order: order++,
      })
    }
  }

  for (const [key, swatch] of Object.entries(swatchTable)) {
    if (dynamicSlots.includes(key)) continue
    if (RESERVED_SLOTS.includes(key)) continue
    schemas.push({
      key: `swatch.${key}`,
      propertyKey: "color",
      label: swatchSchemaLabel(
        swatch as StockThemeSwatch | ThemeSwatch,
        theme,
      ),
      section: "swatch",
      order: order++,
    })
  }

  return schemas
}

/**
 * Generate schemas for shadow properties
 */
export function generateShadowSchemas(
  theme: ThemeOrStock,
): ThemeTokenSchemaUnresolved[] {
  const schemas: ThemeTokenSchemaUnresolved[] = []
  let order = 0

  Object.entries(theme.shadow).forEach(([shadowId, shadow]) => {
    if (!shadow) return
    const baseOrder = order * 5

    schemas.push(
      {
        key: `shadow.${shadowId}.offsetX`,
        propertyKey: "shadowOffsetX",
        label: `${shadow.name} - Offset X`,
        section: "shadow",
        order: baseOrder,
      },
      {
        key: `shadow.${shadowId}.offsetY`,
        propertyKey: "shadowOffsetY",
        label: `${shadow.name} - Offset Y`,
        section: "shadow",
        order: baseOrder + 1,
      },
      {
        key: `shadow.${shadowId}.blur`,
        propertyKey: "shadowBlur",
        label: `${shadow.name} - Blur`,
        section: "shadow",
        order: baseOrder + 2,
      },
      {
        key: `shadow.${shadowId}.spread`,
        propertyKey: "shadowSpread",
        label: `${shadow.name} - Spread`,
        section: "shadow",
        order: baseOrder + 3,
      },
      {
        key: `shadow.${shadowId}.color`,
        propertyKey: "shadowColor",
        label: `${shadow.name} - Color`,
        section: "shadow",
        order: baseOrder + 4,
      },
    )

    order++
  })

  return schemas
}

