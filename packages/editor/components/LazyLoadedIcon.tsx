export type LazyLoadedIconProps = SVGAttributes<SVGSVGElement> & {
  iconId?: IconId
}

export function LazyLoadedIcon({ iconId, ...props }: LazyLoadedIconProps) {
  if (!iconId) {
    return <IconMissing {...props} />
  }

  const Icon = map[iconId]
  if (!Icon) {
    return <IconMissing {...props} />
  }

  return <Icon {...props} />
}
