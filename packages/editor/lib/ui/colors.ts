export function hsla(hsl: string, alpha: number) {
  const matches = hsl.match(/\d+/g)
  if (!matches || matches.length !== 3) {
    throw new Error("Invalid HSL color format")
  }
  const [h, s, l] = matches
  return `hsla(${h}, ${s}%, ${l}%, ${alpha})`
}
